-- Tabla 1: zona (sin dependencias, va primero)
CREATE TABLE zona (
    id_zona     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre      VARCHAR2(100) NOT NULL,
    descripcion VARCHAR2(500)
);

-- Tabla 2: especie
CREATE TABLE especie (
    id_especie  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre      VARCHAR2(120) NOT NULL,
    tipo        VARCHAR2(60),
    dieta       VARCHAR2(40),
    peligro_ext VARCHAR2(60),
    CONSTRAINT chk_dieta CHECK (dieta IN ('Carnivoro','Herbivoro','Omnivoro','Insectivoro','Piscivoro')),
    CONSTRAINT chk_peligro CHECK (peligro_ext IN ('Preocupacion menor','Casi amenazado','Vulnerable',
                                                   'En peligro','En peligro critico','Extinto en estado silvestre','Extinto'))
);

-- Tabla 3: animal
CREATE TABLE animal (
    id_animal        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre           VARCHAR2(100) NOT NULL,
    sexo             CHAR(1)       NOT NULL,
    fecha_nacimiento DATE,
    procedencia      VARCHAR2(150),
    estado           VARCHAR2(30)  DEFAULT 'Saludable',
    id_especie       NUMBER        NOT NULL,
    id_zona          NUMBER        NOT NULL,
    CONSTRAINT fk_animal_especie FOREIGN KEY (id_especie) REFERENCES especie(id_especie),
    CONSTRAINT fk_animal_zona    FOREIGN KEY (id_zona)    REFERENCES zona(id_zona),
    CONSTRAINT chk_sexo_animal   CHECK (sexo IN ('M','F')),
    CONSTRAINT chk_estado_animal CHECK (estado IN ('Saludable','En tratamiento','En cuarentena','Fallecido'))
);

-- Tabla 4: visitante
CREATE TABLE visitante (
    id_visitante NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre       VARCHAR2(120) NOT NULL,
    correo       VARCHAR2(150) UNIQUE,
    telefono     VARCHAR2(25),
    pais         VARCHAR2(80)
);

-- Tabla 5: carrito
CREATE TABLE carrito (
    id_carrito NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    codigo     VARCHAR2(20) NOT NULL UNIQUE,
    capacidad  NUMBER       NOT NULL,
    tipo       VARCHAR2(80),
    estado     VARCHAR2(30) DEFAULT 'Disponible',
    CONSTRAINT chk_capacidad_positiva CHECK (capacidad > 0),
    CONSTRAINT chk_estado_carrito     CHECK (estado IN ('Disponible','En uso','Mantenimiento'))
);

-- Tabla 6: guia
CREATE TABLE guia (
    id_guia          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre           VARCHAR2(120) NOT NULL,
    idiomas          VARCHAR2(200),
    telefono         VARCHAR2(25),
    experiencia_anos NUMBER        DEFAULT 0,
    CONSTRAINT chk_experiencia CHECK (experiencia_anos >= 0)
);

-- Tabla 7: recorrido
CREATE TABLE recorrido (
    id_recorrido NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fecha        DATE          NOT NULL DEFAULT SYSDATE,
    hora_inicio  VARCHAR2(8),
    hora_fin     VARCHAR2(8),
    num_personas NUMBER        DEFAULT 1,
    total_pagado NUMBER(12,2)  DEFAULT 0,
    estado       VARCHAR2(30)  DEFAULT 'Reservado',
    id_visitante NUMBER        NOT NULL,
    id_carrito   NUMBER        NOT NULL,
    id_guia      NUMBER        NOT NULL,
    CONSTRAINT fk_recorrido_visitante FOREIGN KEY (id_visitante) REFERENCES visitante(id_visitante),
    CONSTRAINT fk_recorrido_carrito   FOREIGN KEY (id_carrito)   REFERENCES carrito(id_carrito),
    CONSTRAINT fk_recorrido_guia      FOREIGN KEY (id_guia)      REFERENCES guia(id_guia),
    CONSTRAINT chk_personas_positivas CHECK (num_personas > 0),
    CONSTRAINT chk_total_positivo     CHECK (total_pagado >= 0),
    CONSTRAINT chk_estado_recorrido   CHECK (estado IN ('Reservado','Completado','Cancelado'))
);

-- Tabla 8: avistamiento
CREATE TABLE avistamiento (
    id_avistamiento NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_recorrido    NUMBER        NOT NULL,
    id_animal       NUMBER        NOT NULL,
    hora            VARCHAR2(8),
    comportamiento  VARCHAR2(100),
    notas           VARCHAR2(500),
    CONSTRAINT fk_avistamiento_recorrido FOREIGN KEY (id_recorrido) REFERENCES recorrido(id_recorrido),
    CONSTRAINT fk_avistamiento_animal    FOREIGN KEY (id_animal)    REFERENCES animal(id_animal)
);

-- Tabla 9: empleado
CREATE TABLE empleado (
    id_empleado NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre      VARCHAR2(120) NOT NULL,
    cargo       VARCHAR2(80),
    salario     NUMBER(12,2),
    telefono    VARCHAR2(25),
    CONSTRAINT chk_salario_positivo CHECK (salario > 0)
);


