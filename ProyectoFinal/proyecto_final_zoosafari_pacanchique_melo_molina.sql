-- STORED PROCEDURE: registrar_recorrido_completo
CREATE OR REPLACE PROCEDURE registrar_recorrido_completo(
    p_visitante_id   IN NUMBER,
    p_carrito_id     IN NUMBER,
    p_guia_id        IN NUMBER,
    p_num_personas   IN NUMBER,
    p_precio_persona IN NUMBER
) IS
    v_nombre_vis   VARCHAR2(120);
    v_codigo_carr  VARCHAR2(20);
    v_capacidad    NUMBER;
    v_estado_carr  VARCHAR2(30);
    v_nombre_guia  VARCHAR2(120);
    v_total        NUMBER(12,2);
    v_nuevo_id     NUMBER;
BEGIN
    SELECT nombre INTO v_nombre_vis FROM visitante WHERE id_visitante = p_visitante_id;
    SELECT codigo, capacidad, estado INTO v_codigo_carr, v_capacidad, v_estado_carr
    FROM   carrito WHERE id_carrito = p_carrito_id;

    IF v_estado_carr != 'Disponible' THEN
        RAISE_APPLICATION_ERROR(-20010, 'Carrito ' || v_codigo_carr || ' no disponible. Estado: ' || v_estado_carr);
    END IF;
    IF p_num_personas > v_capacidad THEN
        RAISE_APPLICATION_ERROR(-20011, 'Capacidad excedida: ' || v_codigo_carr || ' max ' || v_capacidad || ' personas.');
    END IF;
    IF p_num_personas <= 0 THEN
        RAISE_APPLICATION_ERROR(-20012, 'El numero de personas debe ser mayor a cero.');
    END IF;

    SELECT nombre INTO v_nombre_guia FROM guia WHERE id_guia = p_guia_id;
    v_total := p_num_personas * p_precio_persona;

    INSERT INTO recorrido (fecha, hora_inicio, num_personas, total_pagado, estado, id_visitante, id_carrito, id_guia)
    VALUES (SYSDATE, TO_CHAR(SYSDATE,'HH24:MI:SS'), p_num_personas, v_total, 'Reservado', p_visitante_id, p_carrito_id, p_guia_id)
    RETURNING id_recorrido INTO v_nuevo_id;

    UPDATE carrito SET estado = 'En uso' WHERE id_carrito = p_carrito_id;
    COMMIT;

    DBMS_OUTPUT.PUT_LINE('=== RECORRIDO #' || v_nuevo_id || ' CREADO ===');
    DBMS_OUTPUT.PUT_LINE('Visitante: ' || v_nombre_vis || ' | Guia: ' || v_nombre_guia);
    DBMS_OUTPUT.PUT_LINE('Carrito: ' || v_codigo_carr || ' | Personas: ' || p_num_personas);
    DBMS_OUTPUT.PUT_LINE('Total: $ ' || TO_CHAR(v_total,'999,999,999'));
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE('ERROR: Visitante, carrito o guia no encontrado.');
    WHEN OTHERS THEN
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE('ERROR: ' || SQLERRM);
END registrar_recorrido_completo;
/

