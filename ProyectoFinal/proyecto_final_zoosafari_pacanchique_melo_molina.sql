-- VISTA 1 DE SEGURIDAD: v_ficha_publica_animal
CREATE OR REPLACE VIEW v_ficha_publica_animal AS
SELECT
    a.id_animal,
    a.nombre                                      AS animal,
    a.sexo,
    FLOOR(MONTHS_BETWEEN(SYSDATE, a.fecha_nacimiento) / 12) AS edad_anos,
    e.nombre                                      AS especie,
    e.tipo                                        AS tipo_animal,
    e.dieta,
    e.peligro_ext                                 AS estado_conservacion,
    z.nombre                                      AS zona_safari,
    CASE
        WHEN a.estado = 'Saludable'        THEN 'Activo para avistamiento'
        WHEN a.estado = 'En tratamiento'   THEN 'Temporalmente fuera de ruta'
        WHEN a.estado = 'En cuarentena'    THEN 'No disponible'
        ELSE                                    'Informacion no disponible'
    END                                           AS disponibilidad_publica
FROM animal  a
JOIN especie e ON a.id_especie = e.id_especie
JOIN zona    z ON a.id_zona    = z.id_zona
WHERE a.estado != 'Fallecido';

SELECT * FROM v_ficha_publica_animal ORDER BY zona_safari, especie;

-- VISTA 2 DE REPORTE: v_reporte_recorridos_por_guia
CREATE OR REPLACE VIEW v_reporte_recorridos_por_guia AS
SELECT
    g.nombre                                      AS guia,
    g.idiomas,
    g.experiencia_anos,
    COUNT(r.id_recorrido)                         AS total_recorridos,
    SUM(r.num_personas)                           AS total_visitantes_atendidos,
    SUM(r.total_pagado)                           AS ingresos_generados,
    ROUND(AVG(r.total_pagado), 0)                 AS promedio_por_recorrido,
    ROUND(AVG(r.num_personas), 1)                 AS promedio_personas,
    MAX(r.fecha)                                  AS ultimo_recorrido,
    COUNT(DISTINCT av.id_avistamiento)            AS avistamientos_registrados
FROM guia g
JOIN recorrido    r  ON g.id_guia      = r.id_guia
JOIN avistamiento av ON r.id_recorrido = av.id_recorrido
WHERE r.estado = 'Completado'
GROUP BY g.nombre, g.idiomas, g.experiencia_anos
ORDER BY ingresos_generados DESC;

SELECT * FROM v_reporte_recorridos_por_guia;

SELECT guia, total_recorridos, ingresos_generados
FROM   v_reporte_recorridos_por_guia
WHERE  experiencia_anos > 5;


