    PRAGMA EXCEPTION_INIT(CARRITO_BLOQUEADO, -54);
BEGIN
    DBMS_OUTPUT.PUT_LINE('=== ESCENARIO NOWAIT: Reservando carrito SAF-01 ===');
    SELECT codigo, estado
    INTO   v_codigo, v_estado_carrito
    FROM   carrito
    WHERE  id_carrito = v_id_carrito
    FOR UPDATE NOWAIT;
    DBMS_OUTPUT.PUT_LINE('Lock obtenido: Carrito ' || v_codigo || ' | Estado: ' || v_estado_carrito);
    IF v_estado_carrito = 'Disponible' THEN
        UPDATE carrito SET estado = 'En uso' WHERE id_carrito = v_id_carrito;
        COMMIT;
        DBMS_OUTPUT.PUT_LINE('EXITO: Carrito ' || v_codigo || ' asignado. Estado -> En uso.');
    ELSE
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE('RECHAZADO: Estado actual ' || v_estado_carrito);
    END IF;
EXCEPTION
    WHEN CARRITO_BLOQUEADO THEN
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE('ALERTA: Carrito bloqueado por otra sesion (ORA-00054). ROLLBACK ejecutado.');
    WHEN OTHERS THEN
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE('ERROR CRITICO: ' || SQLERRM);
END;
/

UPDATE carrito SET estado = 'Disponible' WHERE id_carrito = 1;
COMMIT;

